#import "NSArray+CleanString.h"

@implementation NSArray (CleanString)

- (NSString *)cleanString
{
    // If we get an array in some place we're expected a string, just return an empty string
    // See: https://anghami.facebook.com/groups/anghamiios/permalink/470727473402731/
    return @"";
}

@end
